def extract_section(text: str, section_name: str) -> str:
    lines = text.split('\n')
    result = []
    in_section = False
    
    for line in lines:
        if section_name in line:
            in_section = True
            continue
        if in_section and line.strip().startswith('#') and len(line.strip()) > 2:
            break
        if in_section:
            result.append(line)
    
    if result:
        return '\n'.join(result).strip()
    return text[:500]

